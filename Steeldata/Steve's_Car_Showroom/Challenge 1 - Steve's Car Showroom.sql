--1. What are the details of all cars purchased in the year 2022?

SELECT c.*, s.purchase_date
FROM cars c
JOIN sales s
ON c.car_id = s.car_id
WHERE DATEPART(year,purchase_date) = 2022
ORDER BY purchase_date

--2. What is the total number of cars sold by each salesperson?

WITH cars_sales AS (
SELECT sp.*
FROM sales s 
JOIN salespersons sp
ON s.salesman_id = sp.salesman_id)
SELECT salesman_id, name, COUNT(1) AS no_of_cars_sold
FROM cars_sales
GROUP BY salesman_id, name
ORDER BY salesman_id

--3. What is the total revenue generated by each salesperson?

WITH Revenue AS (
SELECT s.salesman_id,sp.name,c.make,c.cost_$
FROM sales s
JOIN cars c
ON s.car_id = c.car_id
JOIN salespersons sp
ON s.salesman_id = sp.salesman_id)
SELECT salesman_id, name, SUM(cost_$) AS total_revenue_$
FROM Revenue
GROUP BY salesman_id, name

--4. What are the details of the cars sold by each salesperson?

SELECT s.purchase_date,s.salesman_id, sp.name, c.*
FROM cars c
JOIN sales s
ON c.car_id = s.car_id
JOIN salespersons sp ON
s.salesman_id = sp.salesman_id
ORDER BY salesman_id, car_id

--5. What is the total revenue generated by each car type?
WITH car_type AS (
SELECT c.*
FROM cars c
JOIN sales s
ON c.car_id = s.car_id)
SELECT type, SUM(cost_$) AS total_sales
FROM car_type
GROUP BY type
ORDER BY total_sales DESC

--6. What are the details of the cars sold in the year 2021 by salesperson 'Emily Wong'?

SELECT c.*
FROM salespersons sp
JOIN sales s
ON sp.salesman_id = s.salesman_id
JOIN cars c
ON s.car_id = c.car_id
WHERE sp.name = 'Emily Wong' AND DATEPART(year,s.purchase_date) = 2021


--7. What is the total revenue generated by the sales of hatchback cars?

SELECT SUM(cost_$) AS hatchback_sales_$
FROM cars c
JOIN sales s
ON c.car_id = s.car_id
WHERE c.style = 'Hatchback'

--8. What is the total revenue generated by the sales of SUV cars in the year 2022?

SELECT SUM(cost_$) AS SUV_cars_sales_$
FROM cars c
JOIN sales s
ON c.car_id = s.car_id
WHERE c.style = 'SUV' AND DATEPART(year,s.purchase_date) = 2022


--9. What is the name and city of the salesperson who sold the most number of cars in the year 2023?

WITH max_sales AS (
SELECT s.purchase_date, sp.salesman_id, sp.name, sp.city
FROM sales s
JOIN salespersons sp
ON s.salesman_id = sp.salesman_id)
SELECT TOP 1 name AS salesman_name, city, COUNT(1) AS no_of_cars_sold
FROM max_sales
WHERE DATEPART(year,purchase_date) = 2023
GROUP BY DATEPART(year,purchase_date), name, city
ORDER BY no_of_cars_sold DESC

10. What is the name and age of the salesperson who generated the highest revenue in the year 2022?

WITH sp_sales AS (
SELECT s.purchase_date, sp.name, sp.age,c.cost_$
FROM salespersons sp
JOIN sales s
ON sp.salesman_id = s.salesman_id
JOIN cars c
ON s.car_id = c.car_id)
SELECT Top 1 name, age, SUM(cost_$) AS revenue_earned_2022
FROM sp_sales
WHERE datepart(year,purchase_date) = 2022
GROUP BY name, age
ORDER BY revenue_earned_2022 DESC


